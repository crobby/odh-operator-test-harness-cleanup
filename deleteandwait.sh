function oc::wait::object::unavailability() {
    local cmd=$1 # Command whose output we require
    local interval=$2 # How many seconds to sleep between tries
    local iterations=$3 # How many times we attempt to run the command

    ii=0

    while [ $ii -le $iterations ]
    do

        token=$($cmd) && returncode=$? || returncode=$?
        if [ $returncode -ne 0 ]; then
            break
        fi

        ((ii=ii+1))
        if [ $ii -eq 100 ]; then
            echo $cmd "did not return a value"
            exit 1
        fi
        sleep $interval
    done
    echo $token
}

/usr/local/bin/oc create -f /tmp/delconfigmap.yaml

oc::wait::object::unavailability "/usr/local/bin/oc get -n redhat-ods-applications postgres" 180 7200
exit 0